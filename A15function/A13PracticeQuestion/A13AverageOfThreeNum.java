package A15function.A13PracticeQuestion;

public class A13AverageOfThreeNum {
    public static int Average(int a, int b, int c) {
        int average = (a + b + c) / 3;
        return average;
    }

    public static void main(String args[]) {
        System.out.println(Average(6, 7, 10));
    }
}
